#include <iostream>
using namespace std;

// --------------------------------------------------------------------
// numero: N -> f(esPositivo) -> B(true) / B(false)
// --------------------------------------------------------------------

int esPositivo(int num){

    if(num >= 0){
        
        cout << "El numero ingresado es positvo :) ";

        return true;
    
    }

    else{

        cout << "El numero ingresado es negativo :( ";

        return false;

    }

}

// --------------------------------------------------------------------
// --------------------------------------------------------------------


int main(){

    cout << "Ingresa un numero: ";

    int num;

    cin >> num;

    esPositivo(num);

};

