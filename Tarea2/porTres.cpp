#include <iostream>
using namespace std;

// --------------------------------------------------------------------
// numero: N -> f(porTres) -> N 
// --------------------------------------------------------------------

int porTres(int numero){

    return numero * 3;

};

// --------------------------------------------------------------------
// --------------------------------------------------------------------

int main(){
    int numero;

    cout << "Ingrese un numero : ";

    cin >> numero;

    int resultado = porTres(numero);

    cout << "El el resultado del numero por tres es: " << resultado << std::endl;

    return 0 ;

};

